#!/usr/bin/python3
def add(a, b):
    a = 10
    b = 5
    print("{} + {} = {}".format(a, b, a + b))
    

def sub(a, b):
    a = 10
    b = 5
    print("{} + {} = {}".format(a, b, a - b))
    

def mul(a, b):
    a = 10
    b = 5
    print("{} + {} = {}".format(a, b, a * b))
    

def div(a, b):
    a = 10
    b = 5
    print("{} + {} = {:.0f}".format(a, b, a / b)) 
    
add(10, 5)
sub(10, 5)
mul(10, 5)
div(10, 5)
